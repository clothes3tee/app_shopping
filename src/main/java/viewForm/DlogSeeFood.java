    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (MessageDialogHelper.showConfirmDialog(this, "Bạn có muốn xóa thực phẩm này hay không?", "Câu hỏi") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            ThucPham tp = new ThucPham();
            ThucPhamDao dao = new ThucPhamDao();
            if (dao.findByID(lblMaTP.getText()) == null) {
                MessageDialogHelper.showWarningDialog(this, "Bạn không thể xóa bởi vì thực phẩm\n" + "này không có trong danh sách.\n" + "", "Cảnh báo");
                return;
            }
            String id = new String();
            id = nextThucPham();
            if (id == null) {
                id = backThucPham();
            }
            if (dao.delete(lblMaTP.getText())) {
                MessageDialogHelper.showMessageDialog(this, "Thực phẩm đã được xóa thành công.", "Thông báo");
                if (id == null) {
                    setVisible(false);
                    this.dispose();
                }
                try {
                    showThucPham(id);
                } catch (Exception ex) {
                    Logger.getLogger(DlogSeeFood.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                MessageDialogHelper.showMessageDialog(this, "Thực phẩm không được xóa!", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, "Đã xảy ra lôi khi xóa thực phẩm!\nBạn hãy thử xóa lại.", "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

