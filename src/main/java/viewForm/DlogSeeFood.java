    public void showThucPham(String id) throws Exception {

        ThucPhamDao dao = new ThucPhamDao();
        ThucPham tp = dao.findByID(id);
        if (tp != null) {
            lblMaTP.setText(tp.getMaThucPham());
            lblTenThucPham.setText(tp.getTenThucPham());
            rdbRauCu.setSelected(tp.getLoaiThucPham().equals(rdbRauCu.getText()) ? true : false);
            rdbThitCa.setSelected(tp.getLoaiThucPham().equals(rdbThitCa.getText()) ? true : false);
            rdbHat.setSelected(tp.getLoaiThucPham().equals(rdbHat.getText()) ? true : false);
            if (tp.getKhoiLuongTP() >= 1) {
                lblKhoiLuong.setText(valueOf(tp.getKhoiLuongTP()) + " (kg)");
            } else {
                lblKhoiLuong.setText(valueOf((int)(tp.getKhoiLuongTP()*1000)) + " (g)");
            }
            if (tp.getKhoiLuongIt() >= 1) {
                lblKhoiLuongIt.setText(valueOf(tp.getKhoiLuongIt()) + " (kg)");
            } else {
                lblKhoiLuongIt.setText(valueOf((int)(tp.getKhoiLuongIt()*1000)) + " (g)");
            }
            lblGiaTien.setText(valueOf((int) tp.getGiaTienTP() * 1000) + " VND");
            lblGTDD.setText(valueOf(tp.getGiaTriDD()) + " calo");
        }
    }

    public String nextThucPham() {
        try {
            ThucPhamDao dao = new ThucPhamDao();
            ArrayList<ThucPham> list = dao.findAll();
            for (ThucPham it : list) {
                if (it.getMaThucPham().equals(lblMaTP.getText())) {
                    int i = list.indexOf(it) + 1;
                    return list.get(i).getMaThucPham();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

    public String backThucPham() {
        try {
            ThucPhamDao dao = new ThucPhamDao();
            ArrayList<ThucPham> list = dao.findAll();
            for (ThucPham it : list) {
                if (it.getMaThucPham().equals(lblMaTP.getText())) {
                    int i = list.indexOf(it) - 1;
                    return list.get(i).getMaThucPham();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

